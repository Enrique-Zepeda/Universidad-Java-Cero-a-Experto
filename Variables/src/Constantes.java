public class Constantes {
    public static void main(String[] args) {
        System.out.println("*** Constantes en Java ***");
        final var DIAS_SEMANA = 7;
        final int DIAS_SEMANA1 = 7;
        System.out.println("DIAS_SEMANA = " + DIAS_SEMANA);
        System.out.println("DIAS_SEMANA1 = " + DIAS_SEMANA1);
//        DIAS_SEMANA = 8; //no se puede cambiar el valor de una constante
        final var PI = 3.1416;
        System.out.println("PI = " + PI);
        System.out.println("Math.PI = " + Math.PI);
    }
}
